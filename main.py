class RefineryService:
    """
    Ingestion Engine.
    Responsible for transforming raw text into structured ChatEntries.
    """

    # PROMPTS (FROZEN to ensure semantic stability)
    AGENTS: Final[dict[str, str]] = {
        "CRONOS": """ROLE: TIME KEEPER.\nTASK: Extract the LATEST date (YYYY-MM-DD) from the IN content.\nOUTPUT_FORMAT: Respond SOLELY with a JSON object adhering to the provided schema.\nJSON_SCHEMA: {"type": "object", "properties": {"final_timestamp": {"type": "string", "format": "date", "description": "The LATEST date found in the text in YYYY-MM-DD format."}}, "required": ["final_timestamp"]}\nIN:""",
        "SYNTHESIZER": """ROLE: EXECUTIVE SUMMARY GENERATOR.\nTASK: Generate a concise title, an executive summary, and determine the status (CURRENT|LEGACY) based on the IN content.\nOUTPUT_FORMAT: Respond SOLELY with a JSON object adhering to the provided schema.\nJSON_SCHEMA: {"type": "object", "properties": {"title": {"type": "string", "description": "A concise title for the content."}, "summary": {"type": "string", "description": "An executive summary of the content."}, "status": {"type": "string", "enum": ["CURRENT", "LEGACY"], "description": "Indicates if the content is current or legacy."}}, "required": ["title", "summary", "status"]}\nIN:""",
        "NARRATIVE": "ROLE: NARRATIVE ARCHIVIST. JSON {'narrative_context': 'String', 'atmosphere': 'String'}",
        "CLASSIFIER": "ROLE: CATEGORIZER. Context: {cats}. JSON {'suggested_category': 'String'}",
        "LOGOS": "ROLE: BELIEF EXTRACTOR. JSON {'mental_models': [str], 'beliefs': [str]}",
        "PATHOS": "ROLE: EMOTION SENSOR. JSON {'tone': 'String', 'anchors': [str]}",
        "TECHNE": "ROLE: TOOL EXTRACTOR. JSON {'tools': [str], 'solutions': [str]}",
        "POIESIS": "ROLE: MUSE. JSON {'references': [str], 'aesthetics': [str]}",
        "GEM": "ROLE: ANOMALY HUNTER. JSON {'gems': [str], 'trivia': [str]}",
        "INSIGHT_ENGINE": "ROLE: INSIGHT ARCHITECT. TASK: Analyze the provided 'dimensions' JSON. Synthesize a SINGLE, DENSE sentence identifying strictly NEW skills, beliefs, or goals demonstrated by Roger. IGNORE trivial chat. OUTPUT: Raw text string only. No JSON."
    }

    def __init__(self, config: AppConfig, llm: LLMClientProtocol):
        self.config = config
        self.llm = llm
        self.known_hashes = self._load_history()

    def _load_history(self) -> set[str]:
        hashes = set()
        if self.config.ledger_file.exists():
            try:
                with open(self.config.ledger_file, 'r', encoding='utf-8') as f:
                    for line in f:
                        if not line.strip(): continue
                        try:
                            entry = json.loads(line)
                            hashes.add(entry.get('id_hash'))
                        except json.JSONDecodeError: continue
            except IOError as e:
                logging.error(f"Could not read ledger: {e}")
        return hashes

    def process_inbox(self) -> None:
        files = list(self.config.input_dir.glob("*.txt"))
        logging.info(f"Refinery: Processing {len(files)} files.")

        splitter = re.compile(r"(?:##\s*)?(?:Chat|CHAT):\s*.*?|\n(?=Pesta\u00f1a \d+)", re.I | re.DOTALL)

        for file_path in files:
            self._process_file(file_path, splitter)

    def _process_file(self, path: Path, splitter: re.Pattern) -> None:
        try:
            content = path.read_text(encoding='utf-8', errors='ignore')
        except IOError as e:
            logging.error(f"Read Error {path.name}: {e}")
            return

        fragments = [f.strip() for f in splitter.split(content) if f.strip()] or [content.strip()]

        for raw_frag in fragments:
            clean_text = RobustParser.clean_ui_garbage(raw_frag)
            if len(clean_text) < 50: continue

            content_hash = hashlib.md5(clean_text.encode()).hexdigest()
            if content_hash in self.known_hashes:
                continue

            entry = self._synthesize_entry(content_hash, clean_text)
            if entry:
                SafeIO.safe_append(self.config.ledger_file, entry.to_json())
                self.known_hashes.add(entry.id_hash)
                logging.info(f"Ingested: {entry.id_hash[:8]} | {entry.title}")

    def _synthesize_entry(self, uid: str, text: str) -> ChatEntry | None:
        """
        Orchestrates the multi-agent analysis pipeline.
        Returns None if critical metadata (Date/Title) cannot be extracted.
        """
        current_quality_score = "3"

        # 1. CRONOS (Critical: Date)
        cronos_prompt = f"{self.AGENTS['CRONOS']}{text[:10000]}"
        c_resp = self.llm.generate(self.config.agent_model, cronos_prompt)
        c_data = RobustParser.extract_json(c_resp)

        if not c_data.get("final_timestamp"):
            logging.warning(f"Skip {uid[:8]}: No Date extracted.")
            return None

        # 2. SYNTHESIZER (Critical: Summary)
        synthesizer_prompt = f"{self.AGENTS['SYNTHESIZER']}{text[:20000]}"
        s_resp = self.llm.generate(self.config.agent_model, synthesizer_prompt)
        s_data = RobustParser.extract_json(s_resp)

        if not s_data.get("title") or not s_data.get("summary"):
            s_data = {"title": "Untitled (Parse Error)", "summary": "Automatic summary failed.", "status": "CURRENT"}
            current_quality_score = "2"

        # 3. DIMENSIONS (Optional but valuable)
        dims = {}

        # Narrative
        n_resp = self.llm.generate(self.config.agent_model, f"{self.AGENTS['NARRATIVE']}\nIN:\n{text[:20000]}")
        n_data = RobustParser.extract_json(n_resp)
        dims['narrative'] = n_data if n_data else {"narrative_context": "Failed", "atmosphere": "Unknown"}

        # Classifier
        try:
            cats = "['General', 'Philosophy', 'Engineering', 'Strategy', 'Science', 'Entertainment']"
            cl_resp = self.llm.generate(self.config.agent_model, self.AGENTS['CLASSIFIER'].format(cats=cats) + f"\nIN:\n{text[:5000]}")
            assigned_category = RobustParser.extract_json(cl_resp).get("suggested_category", "General")
        except Exception:
            assigned_category = "General"

        # Parallel-ish Extraction
        for agent_key in ["LOGOS", "PATHOS", "TECHNE", "POIESIS", "GEM"]:
            try:
                resp = self.llm.generate(self.config.agent_model, f"{self.AGENTS[agent_key]}\nIN:\n{text[:15000]}")
                dims[agent_key.lower()] = RobustParser.extract_json(resp)
            except Exception:
                dims[agent_key.lower()] = {}

        # 4. INSIGHT (The "So What?")
        relevant_insight = ""
        try:
            insight_prompt = f"{self.AGENTS['INSIGHT_ENGINE']}\nDIMENSIONS_CONTEXT: {json.dumps(dims, ensure_ascii=False)}"
            insight_raw = self.llm.generate(self.config.agent_model, insight_prompt)
            relevant_insight = RobustParser.clean_ui_garbage(insight_raw) if insight_raw else ""

            # Logic Preserved: Downgrade score if insight fails, unless already bad.
            if not relevant_insight and current_quality_score != "1":
                current_quality_score = "2"
        except Exception:
            relevant_insight = "Insight generation failed."

        return ChatEntry(
            id_hash=uid,
            origin_date=c_data.get("final_timestamp", datetime.now().strftime("%Y-%m-%d")),
            full_content=text,
            title=s_data.get("title", "Untitled"),
            assigned_category=assigned_category,
            quality_score=current_quality_score,
            temporal_inference=s_data.get("status", "CURRENT"),
            executive_summary=s_data.get("summary", "No summary"),
            ingestion_timestamp=datetime.now().isoformat(),
            vromlix_dimensions=dims,
            xml_relevant_insight=relevant_insight
        )

class MirrorService:
    """
    Module 2: XML Profile Surgery.
    Maintains the Dynamic Profile XML file based on new evidence.
    """
    DEFAULT_SKELETON = """<?xml version=\"1.0\" encoding=\"UTF-8\"?>
<vromlix_profile version="8.1">
    <system_kernel>
        <identity name="VROMLIX" base_version="8.3"/>
    </system_kernel>
    <user_context identity="Roger">
        </user_context>
    <dynamic_patches>
        </dynamic_patches>
</vromlix_profile>"""

    def __init__(self, config: AppConfig, llm: LLMClientProtocol):
        self.config = config
        self.llm = llm

    def run_surgery(self) -> None:
        if not self.config.ledger_file.exists(): return

        self._ensure_profile()
        evidence = self._gather_evidence()
        if not evidence: return

        current_patches = self._read_patches()

        prompt = f"""
        ROLE: MEMORY SURGEON (System State Updater).
        TASK: EVOLVE 'Dynamic Patches' XML based on NEW EVIDENCE using a CONSERVATIVE MERGE STRATEGY.

        INPUTS:
        1. CURRENT_XML: The existing profile.
        2. NEW_EVIDENCE: Recent insights.

        RULES:
        - PRESERVE: Do NOT delete existing static data unless strictly contradicted.
        - UPDATE: Add new skills to <tech_stack>, update beliefs.
        - FORMAT: Return ONLY the content inside <dynamic_patches>.

        CURRENT_XML:
        {current_patches}

        NEW_EVIDENCE:
        {evidence}
        """

        resp = self.llm.generate(self.config.system_model, prompt)
        if resp and "NO_CHANGES" not in resp:
            self._apply_patch(resp)

    def _ensure_profile(self) -> None:
        if not self.config.profile_file.exists():
            SafeIO.atomic_write(self.config.profile_file, self.DEFAULT_SKELETON)

    def _read_patches(self) -> str:
        try:
            tree = ET.parse(self.config.profile_file)
            root = tree.getroot()
            context_nodes = []
            for tag in ["active_status", "cognitive_model"]:
                node = root.find(tag)
                if node is not None:
                    context_nodes.append(ET.tostring(node, encoding='unicode'))
            
            patches_node = root.find("dynamic_patches")
            patches_str = "".join([ET.tostring(c, encoding='unicode') for c in patches_node]) if patches_node is not None else ""
            
            return "\n".join(context_nodes) + "\n<dynamic_patches>\n" + patches_str + "\n</dynamic_patches>"
        except ET.ParseError:
            return ""

    def _gather_evidence(self) -> str:
        """Reads last 200 entries to form context."""
        lines = []
        try:
            with open(self.config.ledger_file, 'r', encoding='utf-8') as f:
                all_logs = f.readlines()

            targets = all_logs[-200:]
            for line in targets:
                try:
                    d = json.loads(line)
                    insight = d.get('xml_relevant_insight', '').strip()
                    date = d.get('origin_date', 'Unknown')

                    if len(insight) > 10:
                        lines.append(f"[{date}] INSIGHT: {insight}")
                    else:
                        summary = d.get('executive_summary', '')[:100]
                        lines.append(f"[{date}] SUM: {summary}")
                except json.JSONDecodeError: continue
        except IOError: pass
        return "\n".join(lines)

    def _apply_patch(self, xml_fragment: str) -> None:
        """Parses and atomically updates the XML file."""
        try:
            # Clean Markdown blocks
            clean = xml_fragment.replace("```xml", "").replace("```", "").strip()
            # Wrap in dummy root to parse fragments
            new_nodes = ET.fromstring(f"<r>{clean}</r>")

            tree = ET.parse(self.config.profile_file)
            root = tree.getroot()
            patches = root.find("dynamic_patches")
            if patches is None:
                patches = ET.SubElement(root, "dynamic_patches")

            patches.clear()
            for child in new_nodes:
                patches.append(child)

            # Atomic Write via temp string -> SafeIO
            xml_str = ET.tostring(root, encoding='unicode')
            SafeIO.atomic_write(self.config.profile_file, xml_str)
            logging.info("Mirror Surgery: Profile Updated.")

        except ET.ParseError as e:
            logging.error(f"Surgery Failed (XML Parse): {e}. Fragment: {xml_fragment[:50]}...")

class IndexingService:
    """
    Module 3: FAISS Vector Indexing.
    """
    def __init__(self, config: AppConfig):
        self.config = config

    def rebuild_index(self) -> None:
        if not HAS_VECTOR_SUPPORT:
            logging.warning("Vector dependencies missing. Skipping Indexing.")
            return

        if not self.config.ledger_file.exists(): return

        entries = []
        try:
            with open(self.config.ledger_file, 'r', encoding='utf-8') as f:
                for l in f:
                    try: entries.append(json.loads(l))
                    except json.JSONDecodeError: continue
        except IOError: return

        if not entries: return

        # Prepare Text for Embedding
        texts = []
        for e in entries:
            dims = e.get('vromlix_dimensions', {})
            insight = e.get('xml_relevant_insight', '')

            # Safe access to nested dicts
            narrative = dims.get('narrative', {}).get('narrative_context', '')
            tools_list = dims.get('techne', {}).get('tools', [])
            tools_str = RobustParser.safe_join(tools_list)

            text = (
                f"TITLE: {e['title']}. "
                f"INSIGHT: {insight}. "
                f"SUM: {e['executive_summary']}. "
                f"WHY: {narrative}. "
                f"TOOLS: {tools_str}."
            )
            texts.append(text)

        logging.info(f"Generating Embeddings for {len(texts)} entries...")
        model = SentenceTransformer('all-MiniLM-L6-v2')
        embeddings = model.encode(texts)

        # FAISS Indexing
        index = faiss.IndexFlatIP(embeddings.shape[1])
        faiss.normalize_L2(embeddings.astype('float32'))
        index.add(embeddings.astype('float32'))

        self._write_csv_and_update_links(entries, index, embeddings)

    def _write_csv_and_update_links(self, entries: list[dict], index: Any, embeddings: Any) -> None:
        D, I = index.search(embeddings.astype('float32'), k=6)

        updated_ledger = []

        # Write CSV
        try:
            with open(self.config.index_file, 'w', newline='', encoding='utf-8') as csvfile:
                writer = csv.writer(csvfile)
                writer.writerow(["ID", "Date", "Category", "Title", "Summary", "Context", "Connections"])

                for idx, entry in enumerate(entries):
                    # Find Latent Connections
                    links = []
                    for rank, neighbor_idx in enumerate(I[idx]):
                        if idx != neighbor_idx and D[idx][rank] > 0.65:
                            neighbor = entries[neighbor_idx]
                            links.append({
                                "id": neighbor['id_hash'],
                                "title": neighbor['title'],
                                "score": float(D[idx][rank])
                            })

                    entry['latent_connections'] = links[:5]
                    updated_ledger.append(json.dumps(entry, ensure_ascii=False))

                    dims = entry.get('vromlix_dimensions', {})
                    narrative = dims.get('narrative', {}).get('narrative_context', '')[:100]
                    conn_str = ";".join([l['title'] for l in links[:3]])

                    writer.writerow([
                        entry['id_hash'][:8],
                        entry['origin_date'],
                        entry['assigned_category'],
                        entry['title'],
                        entry['executive_summary'],
                        narrative,
                        conn_str
                    ])

        except IOError as e:
            logging.error(f"Failed to write CSV index: {e}")
            return

        # Update Ledger with new connections
        try:
            SafeIO.atomic_write(self.config.ledger_file, '\n'.join(updated_ledger))
            logging.info("Indexing Complete & Ledger Updated.")
        except IOError as e:
            logging.error(f"Failed to update ledger with connections: {e}")

class MaintenanceService:
    """
    Module 4: Sanitation & Hygiene.
    Responsible for keeping the Deep Memory ledger clean and consistent.
    """
    def __init__(self, config: AppConfig):
        self.config = config

    def run_sanitation(self) -> None:
        """
        Scans the ledger for malformed or failed entries ('Untitled', 'No summary')
        and purges them to maintain high-quality vector retrieval.
        """
        if not self.config.ledger_file.exists():
            logging.info("Maintenance: Ledger not found. Nothing to clean.")
            return

        logging.info("Maintenance: Starting Sanitation Protocol...")

        valid_entries = []
        removed_count = 0
        total_count = 0

        # PASS 1: READ & FILTER
        try:
            with open(self.config.ledger_file, 'r', encoding='utf-8') as f:
                for line in f:
                    if not line.strip(): continue
                    total_count += 1
                    try:
                        entry = json.loads(line)

                        # CRITERIA FOR PURGE (The "Bad Data" Signature)
                        is_untitled = entry.get('title') == "Untitled"
                        is_failed_summary = entry.get('executive_summary') == "No summary"
                        has_no_id = not entry.get('id_hash')

                        if is_untitled or is_failed_summary or has_no_id:
                            removed_count += 1
                            # Log sample of deleted data for audit
                            snippet = entry.get('id_hash', 'NO_ID')[:8]
                            logging.debug(f"Purging invalid entry: {snippet}")
                        else:
                            valid_entries.append(line.strip())

                    except json.JSONDecodeError:
                        logging.warning(f"Purging corrupt JSON line: {line[:50]}...")
                        removed_count += 1
                        continue
        except IOError as e:
            logging.error(f"Maintenance Failed (Read): {e}")
            return

        # PASS 2: ATOMIC WRITE (Safety First)
        if removed_count > 0:
            try:
                # Reconstruct the file content
                new_content = "\n".join(valid_entries) + "\n" # Ensure trailing newline
                SafeIO.atomic_write(self.config.ledger_file, new_content)
                logging.info(f"Sanitation Complete. Removed {removed_count} of {total_count} entries.")
            except Exception as e:
                logging.error(f"Maintenance Failed (Write): {e}")
        else:
            logging.info("Sanitation Complete. System is clean.")

