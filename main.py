class TokenBucket:
    """
    Rate Limiter.
    PRESERVED LOGIC: Fixed window reset (every 60s).
    """
    def __init__(self, limit: int) -> None:
        self.limit = limit
        self.used = 0
        self.start_time = time.time()

    def can_consume(self, cost: int) -> bool:
        self._refresh()
        return (self.used + cost) <= self.limit

    def consume(self, cost: int) -> None:
        self._refresh()
        self.used += cost

    def _refresh(self) -> None:
        now = time.time()
        # Logic Warning: This creates a fixed window, not sliding. Preserved per "Chesterton's Fence".
        if now - self.start_time > 60:
            self.start_time = now
            self.used = 0

class GoogleGenAIAdapter(LLMClientProtocol):
    """
    Manages API Keys and Requests.
    Implements Round-Robin rotation.
    """
    def __init__(self, config: AppConfig):
        self.config = config
        self.keys: list[str] = self._discover_keys()
        self.clients: dict[int, Any] = {}
        self.buckets: dict[int, TokenBucket] = {}
        self.current_idx = 0

        if not self.keys:
            logging.warning("No GOOGLE_API_KEYs found. LLM features will be disabled.")
        else:
            self._init_clients()

    def _discover_keys(self) -> list[str]:
        """Scans Env and Userdata for keys following pattern GOOGLE_API_KEY_XX."""
        candidates = ['GOOGLE_API_KEY'] + [f'GOOGLE_API_KEY_{i:02d}' for i in range(1, 20)]
        found = []

        # Check OS Env
        for k in candidates:
            v = os.getenv(k)
            if v and v not in found: found.append(v)

        # Check Colab Userdata
        if HAS_GOOGLE_SDK:
            try:
                for k in candidates:
                    try:
                        v = userdata.get(k)
                        if v and v not in found: found.append(v)
                    except Exception: continue
            except AttributeError: pass

        return found

    def _init_clients(self) -> None:
        if not HAS_GOOGLE_SDK: return
        for i, key in enumerate(self.keys):
            try:
                self.clients[i] = genai.Client(api_key=key)
                self.buckets[i] = TokenBucket(self.config.max_tpm)
            except Exception as e:
                logging.error(f"Failed to init client {i}: {e}")

    def _get_active_client(self, estimated_tokens: int) -> Any:
        """
        Selects a client with available quota.
        If all full, blocks until current key refreshes.
        """
        if not self.clients: return None

        # Try finding a free key
        start_idx = self.current_idx
        for _ in range(len(self.keys)):
            bucket = self.buckets[self.current_idx]
            if bucket.can_consume(estimated_tokens):
                bucket.consume(estimated_tokens)
                return self.clients[self.current_idx]

            # Rotate
            logging.info(f"Key {self.current_idx} saturated. Rotating...")
            self.current_idx = (self.current_idx + 1) % len(self.keys)

        # Fallback: Wait
        logging.warning("All keys saturated. Entering wait state...")
        bucket = self.buckets[self.current_idx]
        wait_time = 61.0 - (time.time() - bucket.start_time)
        if wait_time > 0:
            time.sleep(wait_time)
        bucket.consume(estimated_tokens)
        return self.clients[self.current_idx]

    @override
    def generate(self, model: str, prompt: str) -> str | None:
        if not self.clients: return None

        est_tokens = (len(prompt) // 4) + 500
        client = self._get_active_client(est_tokens)

        # Safety Settings: Unrestricted for creative analysis
        safety_settings = [
            types.SafetySetting(category=cat, threshold="BLOCK_NONE")
            for cat in ["HARM_CATEGORY_HATE_SPEECH", "HARM_CATEGORY_SEXUALLY_EXPLICIT",
                        "HARM_CATEGORY_DANGEROUS_CONTENT", "HARM_CATEGORY_HARASSMENT"]
        ]

        for attempt in range(3):
            try:
                response = client.models.generate_content(
                    model=model,
                    contents=prompt,
                    config=types.GenerateContentConfig(
                        temperature=self.config.temperature,
                        safety_settings=safety_settings
                    )
                )
                return response.text if response.text else None
            except Exception as e:
                err_msg = str(e)
                if "429" in err_msg or "400" in err_msg:
                    logging.warning(f"API Error ({err_msg[:30]}). Rotating key.")
                    self.current_idx = (self.current_idx + 1) % len(self.keys)
                    client = self.clients[self.current_idx]
                    time.sleep(1)
                else:
                    logging.error(f"Fatal GenAI Error: {e}")
                    return None
        return None

