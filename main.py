class TokenBucket:
    """
    Rate Limiter implementing the Token Bucket algorithm.
    Used for proactive API Key rotation.
    """
    def __init__(self, limit: int) -> None:
        self.limit = limit
        self.used = 0
        self.start_time = time.time()

    def can_consume(self, cost: int) -> bool:
        self._refresh()
        return (self.used + cost) <= self.limit

    def consume(self, cost: int) -> None:
        self._refresh()
        self.used += cost

    def _refresh(self) -> None:
        now = time.time()
        if now - self.start_time > 60:
            self.start_time = now
            self.used = 0

class RobustParser:
    """
    Utilities for extracting structured data from 'dirty' LLM outputs.
    Preserves the 'Universal Parsing' logic from legacy v7.5.
    """
    @staticmethod
    def clean_ui_garbage(text: str) -> str:
        """Removes UI artifacts like 'Copy code', 'expand_more'."""
        pattern = re.compile(r"expand_more|content_copy|^python$|^copy code$|^Pesta\u00f1a \d+", re.I | re.M)
        lines = [line for line in text.split('\n') if not pattern.search(line.strip())]
        return "\n".join(lines).strip()

    @staticmethod
    def extract_json(text: str | None) -> dict[str, Any]:
        """Aggressive JSON extraction using Regex fallback."""
        if not text: return {}
        try:
            return json.loads(text)
        except json.JSONDecodeError:
            match = re.search(r'\{.*\}', text, re.DOTALL)
            if match:
                try:
                    return json.loads(match.group())
                except json.JSONDecodeError:
                    # Last resort: Sanitized eval (Legacy fallback)
                    try:
                        sanitized = match.group().replace("true", "True").replace("false", "False").replace("null", "None")
                        # Safety check before eval: only literals
                        # This is risky but required by legacy 'Universal Parsing' spec.
                        # In a stricter environment, we would use ast.literal_eval
                        import ast
                        val = ast.literal_eval(sanitized)
                        if isinstance(val, dict): return val
                    except Exception:
                        pass
        return {}

    @staticmethod
    def safe_join(data: list | str | None) -> str:
        """Flattens mixed lists into a comma-separated string."""
        if not data: return ""
        if isinstance(data, str): return data
        clean_items = []
        for item in data:
            if isinstance(item, str):
                clean_items.append(item)
            elif isinstance(item, dict):
                # Heuristic extraction
                val = next((v for k, v in item.items() if k in {'name', 'tool', 'value', 'model'}), str(item))
                clean_items.append(str(val))
            else:
                clean_items.append(str(item))
        return ", ".join(clean_items)

