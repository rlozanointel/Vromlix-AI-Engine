class RefineryService:
    """Module 1: Ingestion & Metadata Extraction."""

    AGENTS = {
        "CRONOS": "ROLE: TIME KEEPER. TASK: Extract LATEST date (YYYY-MM-DD). JSON {'final_timestamp': 'YYYY-MM-DD'}",
        "SYNTHESIZER": "ROLE: EXECUTIVE SUMMARY. JSON {'title': 'String', 'summary': 'String', 'status': 'CURRENT|LEGACY'}",
        "NARRATIVE": "ROLE: NARRATIVE ARCHIVIST. JSON {'narrative_context': 'String', 'atmosphere': 'String'}",
        "CLASSIFIER": "ROLE: CATEGORIZER. Context: {cats}. JSON {'suggested_category': 'String'}",
        "LOGOS": "ROLE: BELIEF EXTRACTOR. JSON {'mental_models': [str], 'beliefs': [str]}",
        "PATHOS": "ROLE: EMOTION SENSOR. JSON {'tone': 'String', 'anchors': [str]}",
        "TECHNE": "ROLE: TOOL EXTRACTOR. JSON {'tools': [str], 'solutions': [str]}",
        "POIESIS": "ROLE: MUSE. JSON {'references': [str], 'aesthetics': [str]}",
        "GEM": "ROLE: ANOMALY HUNTER. JSON {'gems': [str], 'trivia': [str]}"
    }

    def __init__(self, config: AppConfig, llm: LLMClientProtocol):
        self.config = config
        self.llm = llm
        self.known_hashes = self._load_history()

    def _load_history(self) -> set[str]:
        hashes = set()
        if self.config.ledger_file.exists():
            with open(self.config.ledger_file, 'r', encoding='utf-8') as f:
                for line in f:
                    try:
                        entry = json.loads(line)
                        hashes.add(entry.get('id_hash'))
                    except: continue
        return hashes

    def process_inbox(self) -> None:
        files = list(self.config.input_dir.glob("*.txt"))
        logging.info(f"Refinery: {len(files)} files in tray.")

        splitter = re.compile(r"(?:##\s*)?(?:Chat|CHAT):\s*.*?|\n(?=Pesta\u00f1a \d+)", re.I | re.DOTALL)

        for file_path in files:
            self._process_file(file_path, splitter)

    def _process_file(self, path: Path, splitter: re.Pattern) -> None:
        try:
            content = path.read_text(encoding='utf-8', errors='ignore')
        except Exception as e:
            logging.error(f"Read Error {path.name}: {e}")
            return

        fragments = [f.strip() for f in splitter.split(content) if f.strip()] or [content.strip()]

        for raw_frag in fragments:
            clean_text = RobustParser.clean_ui_garbage(raw_frag)
            if len(clean_text) < 50: continue

            content_hash = hashlib.md5(clean_text.encode()).hexdigest()
            if content_hash in self.known_hashes:
                continue

            entry = self._synthesize_entry(content_hash, clean_text)
            if entry:
                self._persist(entry)

    def _synthesize_entry(self, uid: str, text: str) -> ChatEntry | None:
        # 1. Cronos (Critical)
        c_resp = self.llm.generate(self.config.agent_model, f"{self.AGENTS['CRONOS']}\nIN:\n{text[:10000]}")
        c_data = RobustParser.extract_json(c_resp)
        if not c_data.get("final_timestamp"):
            logging.warning(f"Skip {uid[:8]}: No Date extracted.")
            return None

        # 2. Synthesis & Narrative
        s_resp = self.llm.generate(self.config.agent_model, f"{self.AGENTS['SYNTHESIZER']}\nIN:\n{text[:20000]}")
        n_resp = self.llm.generate(self.config.agent_model, f"{self.AGENTS['NARRATIVE']}\nIN:\n{text[:20000]}")
        s_data = RobustParser.extract_json(s_resp)
        n_data = RobustParser.extract_json(n_resp)

        meta = {**c_data, **s_data}

        # 3. Dimensions
        dims = {'narrative': n_data}
        # Classifier
        try:
            cats = "['General', 'Philosophy', 'Engineering', 'Strategy', 'Science', 'Entertainment']"
            cl_resp = self.llm.generate(self.config.agent_model, self.AGENTS['CLASSIFIER'].format(cats=cats) + f"\nIN:\n{text[:5000]}")
            meta["cat"] = RobustParser.extract_json(cl_resp).get("suggested_category", "General")
        except: meta["cat"] = "General"

        for agent_key in ["LOGOS", "PATHOS", "TECHNE", "POIESIS", "GEM"]:
            resp = self.llm.generate(self.config.agent_model, f"{self.AGENTS[agent_key]}\nIN:\n{text[:15000]}")
            dims[agent_key.lower()] = RobustParser.extract_json(resp)

        return ChatEntry(
            id_hash=uid,
            origin_date=meta.get("final_timestamp", datetime.now().strftime("%Y-%m-%d")),
            full_content=text,
            title=meta.get("title", "Untitled"),
            assigned_category=meta.get("cat", "General"),
            quality_score="3",
            temporal_inference=meta.get("status", "CURRENT"),
            executive_summary=meta.get("summary", "No summary"),
            ingestion_timestamp=datetime.now().isoformat(),
            vromlix_dimensions=dims
        )

    def _persist(self, entry: ChatEntry) -> None:
        with open(self.config.ledger_file, 'a', encoding='utf-8') as f:
            f.write(entry.to_json() + '\n')
        self.known_hashes.add(entry.id_hash)
        logging.info(f"Ingested: {entry.id_hash[:8]} | {entry.title}")

class MirrorService:
    """Module 2: XML Profile Surgery."""

    DEFAULT_SKELETON = """<?xml version="1.0" encoding="UTF-8"?>
<vromlix_profile>
    <static_kernel>
        <identity><name>VROMLIX</name><version>8.0</version></identity>
    </static_kernel>
    <dynamic_patches></dynamic_patches>
</vromlix_profile>"""

    def __init__(self, config: AppConfig, llm: LLMClientProtocol):
        self.config = config
        self.llm = llm

    def run_surgery(self) -> None:
        if not self.config.ledger_file.exists(): return

        self._ensure_profile()
        evidence = self._gather_evidence()
        if not evidence: return

        current_patches = self._read_patches()

        prompt = f"""
        ROLE: MEMORY SURGEON (System State Updater).
        TASK: Evolve 'Dynamic Patches' XML based on new evidence.
        CONTEXT:
        {current_patches}

        NEW EVIDENCE:
        {evidence}

        OUTPUT: Provide ONLY the valid XML content inside <dynamic_patches>.
        If no change needed, return NO_CHANGES.
        """

        resp = self.llm.generate(self.config.system_model, prompt)
        if resp and "NO_CHANGES" not in resp:
            self._apply_patch(resp)

    def _ensure_profile(self) -> None:
        if not self.config.profile_file.exists():
            self.config.profile_file.write_text(self.DEFAULT_SKELETON)

    def _read_patches(self) -> str:
        try:
            tree = ET.parse(self.config.profile_file)
            root = tree.getroot()
            node = root.find("dynamic_patches")
            return "".join([ET.tostring(c, encoding='unicode') for c in node]) if node is not None else ""
        except Exception:
            return ""

    def _gather_evidence(self) -> str:
        lines = []
        try:
            with open(self.config.ledger_file, 'r', encoding='utf-8') as f:
                all_logs = f.readlines()

            # Logic: Priority records + last 20
            targets = []
            for l in all_logs:
                if "PRIORITY" in l or "FUNDACIONAL" in l: targets.append(l)
            targets.extend(all_logs[-20:])

            for line in targets:
                d = json.loads(line)
                dims = d.get('vromlix_dimensions', {})
                tools = RobustParser.safe_join(dims.get('techne', {}).get('tools'))
                beliefs = RobustParser.safe_join(dims.get('logos', {}).get('mental_models'))
                lines.append(f"DATE:{d['origin_date']} | SUM:{d['executive_summary']} | TOOLS:{tools} | BELIEFS:{beliefs}")
        except Exception: pass
        return "\n".join(lines[-30:]) # Limit token usage

    def _apply_patch(self, xml_fragment: str) -> None:
        try:
            # Clean Markdown blocks
            clean = xml_fragment.replace("```xml", "").replace("```", "").strip()
            new_nodes = ET.fromstring(f"<r>{clean}</r>")

            tree = ET.parse(self.config.profile_file)
            root = tree.getroot()
            patches = root.find("dynamic_patches")
            if patches is None: patches = ET.SubElement(root, "dynamic_patches")

            patches.clear()
            for child in new_nodes: patches.append(child)

            tree.write(self.config.profile_file, encoding='unicode')
            logging.info("Mirror Surgery: Profile Updated.")
        except ET.ParseError as e:
            logging.error(f"Surgery Failed (XML Parse): {e}")

class IndexingService:
    """Module 3: FAISS Vector Indexing."""

    def __init__(self, config: AppConfig):
        self.config = config

    def rebuild_index(self) -> None:
        if not HAS_VECTOR_SUPPORT:
            logging.warning("Vector dependencies missing. Skipping Indexing.")
            return

        if not self.config.ledger_file.exists(): return

        entries = []
        with open(self.config.ledger_file, 'r', encoding='utf-8') as f:
            for l in f:
                try: entries.append(json.loads(l))
                except: continue

        if not entries: return

        # Prepare Text
        texts = []
        for e in entries:
            dims = e.get('vromlix_dimensions', {})
            text = (
                f"TITLE: {e['title']}. SUM: {e['executive_summary']}. "
                f"WHY: {dims.get('narrative', {}).get('narrative_context', '')}. "
                f"TOOLS: {RobustParser.safe_join(dims.get('techne', {}).get('tools'))}."
            )
            texts.append(text)

        logging.info("Generating Embeddings...")
        model = SentenceTransformer('all-MiniLM-L6-v2')
        embeddings = model.encode(texts)

        # FAISS
        index = faiss.IndexFlatIP(embeddings.shape[1])
        faiss.normalize_L2(embeddings.astype('float32'))
        index.add(embeddings.astype('float32'))

        # Write CSV & Update Ledger Links
        self._write_csv_and_update_links(entries, index, embeddings)

    def _write_csv_and_update_links(self, entries: list[dict], index: Any, embeddings: Any) -> None:
        D, I = index.search(embeddings.astype('float32'), k=6)

        updated_ledger = []
        with open(self.config.index_file, 'w', newline='', encoding='utf-8') as csvfile:
            writer = csv.writer(csvfile)
            writer.writerow(["ID", "Date", "Category", "Title", "Summary", "Context", "Connections"])

            for idx, entry in enumerate(entries):
                # Find Connections
                links = []
                for rank, neighbor_idx in enumerate(I[idx]):
                    if idx != neighbor_idx and D[idx][rank] > 0.65:
                        neighbor = entries[neighbor_idx]
                        links.append({
                            "id": neighbor['id_hash'],
                            "title": neighbor['title'],
                            "score": float(D[idx][rank])
                        })

                entry['latent_connections'] = links[:5]
                updated_ledger.append(json.dumps(entry, ensure_ascii=False))

                dims = entry.get('vromlix_dimensions', {})
                narrative = dims.get('narrative', {}).get('narrative_context', '')[:100]

                writer.writerow([
                    entry['id_hash'][:8],
                    entry['origin_date'],
                    entry['assigned_category'],
                    entry['title'],
                    entry['executive_summary'],
                    narrative,
                    ";".join([l['title'] for l in links[:3]])
                ])

        # Write back to ledger with new connections
        with open(self.config.ledger_file, 'w', encoding='utf-8') as f:
            f.write('\n'.join(updated_ledger))
        logging.info("Indexing Complete.")


