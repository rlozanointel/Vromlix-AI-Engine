class VromlixOrchestrator:
    def __init__(self, config: AppConfig):
        self.config = config
        self.config.ensure_directories()

        # Configure Logging
        logging.basicConfig(
            level=logging.INFO,
            format='%(asctime)s - [VROMLIX] - %(levelname)s - %(message)s',
            force=True
        )

    def run(self, stages: list[str]) -> None:
        logging.info(f"BOOTING VROMLIX {self.config.system_version}")

        # Mount Drive if Colab
        if Path("/content").exists() and not Path("/content/drive").exists():
            try:
                from google.colab import drive # type: ignore
                drive.mount("/content/drive")
            except ImportError: pass

        llm_client = GoogleGenAIAdapter(self.config)

        if "refinery" in stages:
            RefineryService(self.config, llm_client).process_inbox()

        if "mirror" in stages:
            MirrorService(self.config, llm_client).run_surgery()

        if "index" in stages:
            IndexingService(self.config).rebuild_index()

def main() -> None:
    parser = argparse.ArgumentParser(description="VROMLIX GENESIS: Cognitive Orchestrator")
    parser.add_argument("--run-all", action="store_true", help="Run full pipeline")
    parser.add_argument("--refinery", action="store_true", help="Run Ingestion only")
    parser.add_argument("--mirror", action="store_true", help="Run Profile Surgery only")
    parser.add_argument("--index", action="store_true", help="Run Indexing only")

    args = parser.parse_args([])

    # Default behavior if no args
    stages = []
    if args.run_all or (not any([args.refinery, args.mirror, args.index])):
        stages = ["refinery", "mirror", "index"]
    else:
        if args.refinery: stages.append("refinery")
        if args.mirror: stages.append("mirror")
        if args.index: stages.append("index")

    config = AppConfig()
    try:
        VromlixOrchestrator(config).run(stages)
    except Exception as e:
        logging.critical(f"SYSTEM HALT: {e}", exc_info=True)
        sys.exit(1)

