class SafeIO:
    """
    Encapsulates dangerous I/O operations with atomic guarantees.
    Prevents partial writes.
    """
    @staticmethod
    def atomic_write(target_path: Path, content: str, mode: str = 'w') -> None:
        """Writes to a temp file first, then renames to target."""
        target_path.parent.mkdir(parents=True, exist_ok=True)
        # Create temp file in the same directory to ensure atomic move works
        tmp_name = f"{target_path.name}.tmp"
        tmp_path = target_path.parent / tmp_name

        try:
            with open(tmp_path, mode, encoding='utf-8') as f:
                f.write(content)
            # Atomic replacement
            shutil.move(str(tmp_path), str(target_path))
        except Exception as e:
            if tmp_path.exists():
                os.remove(tmp_path)
            logging.error(f"Atomic write failed for {target_path}: {e}")
            raise IOError(f"Failed to write {target_path}") from e

    @staticmethod
    def safe_append(target_path: Path, line: str) -> None:
        """Appends a line safely with flushing."""
        try:
            with open(target_path, 'a', encoding='utf-8') as f:
                f.write(line + '\n')
        except IOError as e:
            logging.error(f"Append failed for {target_path}: {e}")
            raise

class RobustParser:
    """
    Legacy-compatible parser.
    Preserves the 'dirty' extraction logic but wraps it safely.
    """
    @staticmethod
    def clean_ui_garbage(text: str) -> str:
        """Removes UI artifacts."""
        pattern = re.compile(r"expand_more|content_copy|^python$|^copy code$|^Pesta\u00f1a \d+", re.I | re.M)
        lines = [line for line in text.split('\n') if not pattern.search(line.strip())]
        return "\n".join(lines).strip()

    @staticmethod
    def extract_json(text: str | None) -> dict[str, Any]:
        """
        Extracts JSON from mixed text.
        SECURITY FIX: Replaced raw eval() with ast.literal_eval().
        """
        if not text: return {}

        # 1. Try Direct Parse
        try:
            return json.loads(text)
        except json.JSONDecodeError:
            pass

        # 2. Try Regex Extraction
        match = re.search(r'(\{[\s\S]*\})', text)
        if match:
            json_str = match.group()
            try:
                return json.loads(json_str)
            except json.JSONDecodeError:
                # 3. Last Resort: Python Literal Eval (Sanitized)
                # This handles cases where JSON is actually a Python dict string (Single quotes, None, True/False)
                try:
                    sanitized = (json_str
                                 .replace("true", "True")
                                 .replace("false", "False")
                                 .replace("null", "None"))
                    val = ast.literal_eval(sanitized)
                    if isinstance(val, dict):
                        return val
                except (ValueError, SyntaxError):
                    logging.debug(f"Failed to parse JSON/Dict from text: {text[:50]}...")

        return {}

    @staticmethod
    def safe_join(data: list | str | None) -> str:
        """Flattens mixed lists into a string."""
        if not data: return ""
        if isinstance(data, str): return data
        clean_items = []
        for item in data:
            if isinstance(item, str):
                clean_items.append(item)
            elif isinstance(item, dict):
                # Heuristic: Find the most 'value-like' key
                val = next((v for k, v in item.items() if k in {'name', 'tool', 'value', 'model'}), str(item))
                clean_items.append(str(val))
            else:
                clean_items.append(str(item))
        return ", ".join(clean_items)

